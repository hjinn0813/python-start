## python 기본 문법
# 주석을 해시태그로 표시한다
# 변수의 자료형에 대한 선언 없음 (저장되는 값에 따라 자동으로 자료형 지정됨)
# 문장의 끝에 세미콜론(;) 사용할 필요 없음 (한 줄에 여러 문장일 때만 사용)
num = 1; print(num)

# 코드블록이 있으면 콜론(:)과 여백으로 구분한다
# 변수에 연속해서 값 저장 가능
x,y,z = 10,20,30
print(x + y - z)

