## python 데이터 타입
# 정수 (integer)
print(2)
print(2 + 7)

# 소수 (floating point)
print(2.0)

# 문자열 (string)
print("hello world")
print("2")
print("2" + "7")

# 불리언 (boolean)
print(3 < 7)
print(2 > 5)
