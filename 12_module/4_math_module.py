## python 기본 모듈 - math
import math

print(math.floor(3.14)) # 내림 -> 3
print(math.ceil(3.14)) # 올림 -> 4
print(math.sqrt(9)) # 루트 9 -> 3.0
print(math.fabs(-1)) # 절대값 -> 1.0

